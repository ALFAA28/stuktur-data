    private void tbldataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldataMouseClicked
        DefaultTableModel dtm = (DefaultTableModel) tbldata.getModel(); //create a model
        int selectedRowIndex = tbldata.getSelectedRow(); //get selected row
        int id = (int) dtm.getValueAt(selectedRowIndex, 0); //get the primary key to fetch data.

        Koneksi ks = new Koneksi();
        ks.koneksi();
        try {
            String sql = "select * from tbltransaksi where idTransaksi like '%" + id + "%'";
            pst = ks.connn.prepareStatement(sql);
            ResultSet rs = rs = pst.executeQuery(sql);

            while (rs.next()) {    //u can use normal query

                intidt.setText(rs.getString("IdTransaksi"));
                intidp.setText(rs.getString("IdPenumpang"));
                intnama.setText(rs.getString("namaPenumpang"));
                intalamat.setText(rs.getString("alamat"));
                intjenist.setText(rs.getString("jenisTiket"));
                intjumlah.setText(rs.getString("jmlTiket"));
                intmaskapai.setText(rs.getString("Maskapai"));
                intjenisp.setText(rs.getString("jenisPesawat"));
                intrute.setText(rs.getString("Rute"));
                intjadwal.setText(rs.getString("jadwalTakeOff"));

                String jenis = rs.getString("gender");
                if (jenis.equals("Laki-Laki")) {
                    rLaki.setSelected(true);
                } else {
                    rPerempuan.setSelected(true);
                }
            }
        } catch (Exception ex) {
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_tbldataMouseClicked

